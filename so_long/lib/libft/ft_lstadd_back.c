#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	last = *lst;
	if (!*lst)
		*lst = new;
	else
	{
		while (last && last->next)
			last = last->next;
		last->next = new;
	}
}
